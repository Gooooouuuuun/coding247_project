package kosmo.team.project.dto;

public class MatchingDTO {
	
	private int match_no;
	private String title;
	private int writer;
	private String nickname;
	private String content;
	private int sido_name;
	private int sigungu_name;
	private int readcount;
	private String reg_date;
	private int time_no;
	private String day;
	
	
	public int getMatch_no() {
		return match_no;
	}
	public void setMatch_no(int match_no) {
		this.match_no = match_no;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getWriter() {
		return writer;
	}
	public void setWriter(int writer) {
		this.writer = writer;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getSido_name() {
		return sido_name;
	}
	public void setSido_name(int sido_name) {
		this.sido_name = sido_name;
	}
	public int getSigungu_name() {
		return sigungu_name;
	}
	public void setSigungu_name(int sigungu_name) {
		this.sigungu_name = sigungu_name;
	}
	public int getReadcount() {
		return readcount;
	}
	public void setReadcount(int readcount) {
		this.readcount = readcount;
	}
	public String getReg_date() {
		return reg_date;
	}
	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}
	public int getTime_no() {
		return time_no;
	}
	public void setTime_no(int time_no) {
		this.time_no = time_no;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	
	
	
}
